#include "libft.h"

void		ft_putendl_fd(char const *c, int fd)
{
	size_t i;

	if (!c)
		return ;
	i = 0;
	while (c[i] != '\0')
	{
		write(fd, &c[i], 1);
		i++;
	}
	write(fd, "\n", 1);
}
